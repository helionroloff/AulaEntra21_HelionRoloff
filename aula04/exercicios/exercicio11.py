# Escreva um programa que peça o peso (float) de 3 pessoas e motre na tela a soma de todos os pesos. 

peso1=float(input('informe o peso da pessoa 1'))
peso2=float(input('informe o peso da pessoa 2'))
peso3=float(input('informe o peso da pessoa 3'))
soma=float(peso1+peso2+peso3)
print('a soma do peso das e pessoas é',soma)


