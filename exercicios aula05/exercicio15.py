# Crie um programa que peça ao usuário que digite um número de 1 a 12 e mostre o mês correspondente ao número.
num=int(input('informe um número de 1 a 12 '))


if num ==1:
    print('janeiro')
elif num ==2:
    print('fevereiro')
elif num ==3:
    print('março')
elif num ==4:
    print('abril')
elif num ==5:
    print('maio')
elif num ==6:
    print('junho')
elif num ==7:
    print('julho')
elif num ==8:
    print('agosto')
elif num ==9:
    print('setembro')
elif num ==10:
    print('outubro')
elif num ==11:
    print('novembro')
elif num ==12:
    print('dezembro')
else:
    print('número inválido')
