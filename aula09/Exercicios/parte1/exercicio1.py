

"""Exercicio 01

Faça um programa que conte de 0 a 100 e mostre todos os números.
"""

print('''Automaticamente o programa agora vai contar de 0 a 100
Primeiro de forma vertical:''')
for i in range(101):
    print(i)
print('Agora em exibição horizontal:')
for i in range(101):
    print(i,end='  ')
print('')    
print('FIM')