# Usando as funções da lista responda:
# 
# Quantos itens tem a lista?
# Qual é o número maior?
# Qual é o número menor?
# Qual é o resultado da soma da lista?
# 
# 
# 
lista=[33,5,12,8,60,3,2,6,25,31]
quantidade=len(lista)
maiorvalor=max(lista)
menorvalor=min(lista)
somalista=sum(lista)
print(f'a lista tem {quantidade} itens')
print(f'o maior número é {maiorvalor}')
print(f'o menor número é {menorvalor}')
print(f'a soma de todos os valores da lista é {somalista}')

